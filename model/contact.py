class Contact():
    def __init__(self, nom, email, departement, tel):
        self.nom = nom
        self.email = email
        self.departement = departement
        self.tel = tel

    def setNom(self, nom):
        self.nom = nom

    def setPrenom(self, email):
        self.email = email

    def setDepartement(self, departement):
        self.departement = departement

    def setTel(self, tel):
        self.tel = tel