class Contact():
    def __init__(self, nom, prenom, departement):
        self.nom = nom
        self.prenom = prenom
        self.deparement = departement

    def setNom(self, nom):
        self.nom = nom

    def setPrenom(self, prenom):
        self.prenom = prenom

    def setDepartement(self, departement):
        self.deparement = departement