class Contact():
    def __init__(self, nom, email, dep, tel):
        self.nom = nom
        self.email = email
        self.dep = dep
        self.tel = tel

    def setNom(self, nom):
        self.nom = nom

    def setPrenom(self, email):
        self.email = email

    def setDep(self, dep):
        self.dep = dep

    def setTel(self, tel):
        self.tel = tel